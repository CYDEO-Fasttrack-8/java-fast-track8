package m01_first_program;

/**
 * Print vs println demo
 */

/*
We are learning print vs println
 */

public class PrintAndPrintln {
    public static void main(String[] args) {
        //println prints then adds new line
        System.out.println("Hello");
        System.out.println("Cydeo");

        System.out.println("-------------------");
        //print prints and does not add new line
        System.out.print("Hello");
        System.out.print("Cydeo");
    }
}
