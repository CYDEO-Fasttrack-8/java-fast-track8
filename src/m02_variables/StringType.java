package m02_variables;

public class StringType {
    public static void main(String[] args) {

        String greeting;

        greeting = "Hello World";

        System.out.println(greeting);

        String school = "CYDEO";

        System.out.println(school);

        System.out.println("-------------------------------");

        String name = "James";
        name = "Sarah";
        System.out.println("My name is " + name );
    }
}
