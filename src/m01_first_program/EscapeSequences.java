package m01_first_program;
/*
\" -> "
\n -> new line
\t -> tab
\' -> single quote
\\ -> prints \
 */
public class EscapeSequences {
    public static void main(String[] args) {
        System.out.println("he said \"hello\".");
        //C:\Users\Downloads
        System.out.println("C:\\Users\\Downloads");

        System.out.println("java\n17");

    }
}
